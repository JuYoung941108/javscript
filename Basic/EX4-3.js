var result = 10 + 20;
console.log(result);
// 10+20은 연산을 통해 새로운 값 30을 생성한다.
// 그리고 연산을 통해 생성된 값 30은 메모리 공간에 저장된다.
// 이 때, 메모리 공간에 저장된 값 30을 다시 읽어들여 재사용할 수 있도록
// 값이 저장된 메모리 공간에 상징적인 이름을 붙인것이 바로 변수이다.

// 변수에 값을 저장하는 것을 '할당'(대입, 저장)이라 하고, 변수에 저장된 값을 읽어 들이는 것을 '참조'라한다.

// 변수 == 식별자
// 식별자: 어떤 값을 구별해서 식별할 수 있는 고유한 이름을 말한다.
// 식별자는 값이 저장되어 있는 메모리 주소와 매핑 관계를 맺는다.
// == 매핑 정보도 메모리에 저장되어야 한다.
// == 값이 아니라 메모리 주소를 기억하고 있다.
// == 메모리 주소에 붙인 이름